public class Assigment_Operator {
    public static void main(String[] args) {
        int a=10;
        System.out.println("a= :"+a);
        a+=5;
        System.out.println("a+= :"+a);
        a-=5;
        System.out.println("a-= :"+a);
        a*=5;
        System.out.println("a*= :"+a);
        a/=5;
        System.out.println("a/= :"+a);
        a%=5;
        System.out.println("a%= :"+a);
    }
}
